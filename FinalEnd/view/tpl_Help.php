<p>1. <strong>Einf&uuml;hrung</strong><a name="1"></a></p>
<p>1.1 <b>Was ist Final-End?</b><a name="1_1"></a></p>
<p>Final-end ist ein Echtzeitstrategiespiel, welches online mit Hilfe eines Webbrowsers gespielt werden kann. Final-End bietet dabei die Möglichkeit Allianzen mit anderen Spielern zu gründen, mit ihnen Handel zu treiben oder Gegner anzugreifen und zu unterwerfen.</p>
<p>1.2 <b>Bin drin, was jetzt?</b><a name="1_2"></a></p>
<p>Zu Beginn des Spieles siehst du die Planetenübersicht, mit allen Eigenschaften deines ersten Planeten. Baue zunächst deine Gebäude aus um mehr Rohstoffe zu erhalten.
 Im weiteren Verlauf des Spieles wirst du weitere Gebäude freischalten können die es dir ermöglichen zu forschen und Armeen auszuheben um damit deine Gegenspieler zu attackieren.</p>


