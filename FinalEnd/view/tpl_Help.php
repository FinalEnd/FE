 <p>3 <strong>Menü</strong><a name="3"></a></p>
<p>3.1 <b>Übersicht</b><a name="3_1"></a></p>
<p>Hier findest du alle wichtigen Informationen rund um deinen Planeten. In der linken oberen Ecke kannst du den Planeten wählen den du verwalten möchtest. Auch den Namen des Planeten kann man hier bearbeiten.</p>
<p>3.2 <b>Gebäude</b><a name="3_2"></a></p>
<p>In dieser Übersicht kannst du Gebäude erstellen und verbessern, die es deinem Planeten ermöglichen schneller Ressourcen zu generieren, Schiffe zu bauen (Werft), zu forschen (Labor), Handel zu treiben (Handelszentrum), ihr Lager zu erweitern oder die Flottengröße und Anzahl zu erhöhen (Kommunikationszentrale). </p>
<p>3.3 <b>Forschung</b><a name="3_3"></a></p>
<p>In dieser Übersicht kannst du forschen. Erforsche Upgrades um deine Einheiten zu verbessern.</p>
<p>3.4 <b>Werft</b><a name="3_4"></a></p>
<p>Die Werft ermöglicht es Schiffe zu bauen die dann später zu Flotten zusammen gefasst werden.
Der Maximale Level ist 10. Bauwarteschlangengröße von Level 1 bis 4 beträgt 2, von Level 5 bis 10 wird diese auf 5  Auftäge zur gleichen Zeit erhöht.</p>

<p>3.5 <b>Dock</b><a name="3_6"></a></p>
<p>Hier erstellst du neue Einheiten aus den Schiffen die du in der Werft erstellt hast. Das Dock ermöglicht es dir deine Einheiten mit Rohstoffen zu be-/entladen, um diese zu anderen Planeten transportieren zu können. Außerdem können beschädigte Flotten repariert oder gar aufgelöst werden. Reparatur erfordert je nach Beschädigungsgrad Ressourcen. Beim Auflösen einer Flotte werden die Schiffe wieder in der Werft deponiert,
 jedoch verlieren sie dabei ihren erlangten Kampfrang. Ist die Flotte beschädigt, werden die Schiffe prozentual zerstört und nicht in die Werft überführt.</p>
<p>3.6 <b>Karte</b><a name="3_7"></a></p>
<p>Die Taktische Karte ermöglicht dir deine Einheiten so zu positionieren wie du es für richtig hältst, um deine Gegner auszuschalten. Rechter Hand befinden sich alle eigenen Einheiten und Planeten.
Unterhaltb befinden sich alle Einheiten und Planeten aus dem aktuellen Kartenausschnitt. Wird eine Einheit ausgewählt, so erscheint unter der Kartenausschnittsliste ein Detailbereich mit weiterführenden Informationen. Hier stehen im linken Bereich grundlegende Daten wie Rang, Schaden, Schutz und Beladungsstatus. Unterhalb des verursachten Solds stehen optional gewählte
Upgrades der Flotte die über Forschung erlangt wurden. Rechts wird die Anzahl der einzelnen Schiffstypen innerhalb der Flotte, sowie ein Routensystem festgehalten. Das Routensystem erlaubt wiederkehrende Abläufe automatisch abwickeln zu lassen. </p>
<p>3.7 <b>Nachrichten</b><a name="3_7"></a></p>
<p>Wenn du Nachrichten erhältst kannst du diese hier ansehen und dem jeweiligem Spieler zurück schreiben.</p>
<p>3.8 <b>Allianz</b><a name="3_7"></a></p>
<p>Im Allianzbereich kannst du eine Neue Allianz gründen oder wenn du einer Allianz beigetreten bist mit anderen Allianzmitgliedern wichtige Themen besprechen. Bist du in einer Allianz, werden dir alle Einheiten der Mitglieder auf der Karte angezeigt, selbst wenn keine eigene Einheit in der Nähe sind.</p>

<p>3.9 <b>Einstellungen</b><a name="3_7"></a></p>
<p>Hier kannst du ihr Passwort wechseln</p>

<p>3.10 <b>Logout</b><a name="3_7"></a></p>
<p>Mit dem Logout Button kannst du dich ausloggen um das Spiel zu verlassen. Sie sollten sich aus Sicherheitsgründen immer ausloggen.</p>

<p>3.11 <b>Rangliste</b><a name="3_7"></a></p>
<p>Hier hast du einen Einblick auf alle anderen Spieler und können sich mit Ihnen vergleichen. Der Level eines Spieler wird über Erfahrungspunkte berechnet. Hierbei bekommt man von einer Planetenübernahme 5000 Erfahrungspunkte und je zerstörter feindlicher Flotte, abhängig iher Stärke,  Erfahrungspunkte. Erreicht die Gesamtsumme einen levelabhängigen Wert, so steigen sie im Rang und profitieren von einer Erhöhung des maximalen Kontovolumens von 50.000 Credits.</p>


